"""
i18n — 中英双语支持模块。
扁平 key-value 字典，_() 查询函数，Flask session 存储语言偏好。
"""

from flask import session, request

SUPPORTED_LANGS = {"zh": "中文", "en": "English"}
DEFAULT_LANG = "zh"


def get_lang():
    """从 session/cookie/请求头获取当前语言。"""
    lang = session.get("lang")
    if lang and lang in SUPPORTED_LANGS:
        return lang
    lang = request.cookies.get("lang")
    if lang and lang in SUPPORTED_LANGS:
        return lang
    return DEFAULT_LANG


def set_lang(lang):
    """设置语言到 session。"""
    if lang in SUPPORTED_LANGS:
        session["lang"] = lang


def _(key):
    """翻译查询。key 不存在时返回 key 本身。"""
    lang = get_lang()
    return TRANSLATIONS.get(lang, {}).get(key, TRANSLATIONS.get(DEFAULT_LANG, {}).get(key, key))


def get_all():
    """返回当前语言的全部翻译字典（供模板注入 JS 用）。"""
    lang = get_lang()
    return TRANSLATIONS.get(lang, {})


